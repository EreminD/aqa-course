package ru.inno.course.arrays;

public class Car {
    String color;
    String brand;
    String model;
    int hp;
    String plateNumber;

    public Car(String color, String brand, String model, int hp, String carPlateNumber) {
        this.color = color;
        this.brand = brand;
        this.model = model;
        this.hp = hp;
        plateNumber = carPlateNumber;
    }
}
